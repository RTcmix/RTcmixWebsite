<h3>Synopsis</h3>
<P>
<b>print_on</b>()
<P>


<HR>
<h3>Description</h3>
<P>
<b>print_on()</b> turns on reporting of RTcmix scheduling,
file information, etc.  A subsequent call to
<a href="print_off.php">print_off</a>
will stop RTcmix printing.
<P>


<HR>
<h3>Examples</h3>
<pre>
   print_on()
</pre>
