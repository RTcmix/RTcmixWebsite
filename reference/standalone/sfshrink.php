<b>sfshrink</b> - (what do you suppose this does?)

<hr>

<h3>Usage</h3>

<strong>sfshrink</strong> <i>new_duration_in_seconds filename</i>

<hr>

<h3>Description</h3>

<strong>sfshrink</strong> will indeed chop off the end of a soundfile,
or you can extend (with silence) by specifying a duration longer than the
soundfile. 

