<b>sndreverse</b> - write a soundfile backwards

<hr>

<h3>USAGE</h3>

<strong>sndreverse</strong> <i>filename</i>

<hr>

<h3>DESCRIPTION</h3>

<strong>sndreverse</strong> will write a reversed (backwards-in-time)
version of the soundfile <i>filename</i>, calling it <i>filename.r</i>
(I think).
<p>
I'm not sure how well this one works anymore.  See the
<a href="/reference/instruments/REVMIX.php">REVMIX</a>
instrument if this utility is sad.

