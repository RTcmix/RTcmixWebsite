<b>RTcmix</b> is a real-time software "language" for doing digital sound synthesis and signal-processing.  It is written in C/C++, and is distributed open-source, free of charge. In certain respects, it is similar in function to other extant unit-generator-based software languages such as <a href="http://www.csounds.com">CSOUND</a>, <a href="http://supercollider.sourceforge.net">SuperCollider</a> and (to a lesser extent) <a href="http://www.softsynth.com/jsyn/">JSyn</a> and <a href="http://www.cycling74.com/products/maxmsp.html">Max/MSP</a> -- they do share a common heritage, after all.  There are some differences, however, between all these languages... and <i>variety is</i> of course <i>the spice of life!</i>
<p>
So if you've been searching the web high and low for just the right library of DSP functions to include in your latest&greatest "killer" (or maybe "peacefully coexisting"?) app, then RTcmix may just be the Right Package for You.
<p>
RTcmix is available in several different contexts:
<p>

<ul>
	<li><b><a href="/standalone/">Standalone</a></b><br />
			A command line version that runs on most Unix-like systems, including various flavors of Linux, Mac OS X, IRIX, and FreeBSD.</li><br />

	<li><b><a href="/rtcmix~/">rtcmix~</a></b><br />
	Objects compiled for use in the Max/MSP and Pd graphical programming environments.</li><br />

	<li><b><a href="/irtcmix/">iRTcmix</a></b><br />
	For use in iPhone, iPad and iPod Touch apps.</li><br />
</ul>

	
